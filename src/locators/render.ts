/**
 * Pure rendering functions for the Robot Framework locator generator.
 *
 * These helpers take *already-resolved* data (testid strings, component names)
 * and produce Python source code. Keeping them pure makes the generator easy
 * to snapshot-test and keeps the I/O out of the transformation logic.
 */

import { createHash } from 'node:crypto';
import { renderIdTemplate } from '../util/id-template.js';
import type { RegistryEntry } from '../registry/index.js';
import type { LocatorEntry, LocatorModule } from './types.js';

/** Default Python-variable template — readable even when the testid is pure hash. */
export const DEFAULT_VARIABLE_FORMAT = '{component}_{element}_{key}';

/**
 * Convert a testid segment like `order-list__table--auftragsliste` into a
 * full-path camelCase identifier: `orderListTableAuftragsliste`.
 *
 * The rules are intentionally permissive: any run of non-alphanumeric chars
 * acts as a word boundary, so `__` and `--` and stray underscores all work.
 * A leading digit would be illegal in Python — we prepend `tid` in that case.
 */
export function camelCaseTestid(testid: string): string {
  const parts = testid
    .split(/[^a-zA-Z0-9]+/)
    .filter((p) => p.length > 0)
    .map((p) => p.toLowerCase());
  if (parts.length === 0) return 'tid';
  const [first, ...rest] = parts;
  const joined =
    first! +
    rest
      .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
      .join('');
  return /^[0-9]/.test(joined) ? `tid${joined.charAt(0).toUpperCase()}${joined.slice(1)}` : joined;
}

/** Convert a kebab component name into a snake_case Python filename stem. */
export function filenameForComponent(component: string): string {
  const stem = component.replace(/-/g, '_').replace(/[^a-zA-Z0-9_]/g, '_');
  return `${stem}.py`;
}

/** Build an XPath locator string for a testid. */
export function xpathFor(
  testid: string,
  attributeName: string,
  xpathPrefix: string
): string {
  const xpath = `//*[@${attributeName}='${testid}']`;
  return `${xpathPrefix}${xpath}`;
}

/**
 * Pick the most-specific semantic value from a registry entry for the `{key}`
 * placeholder. Falls back down the priority list; last resort is the tag name
 * itself so locator names stay meaningful even when no semantics were captured.
 */
function primarySemanticValue(entry: RegistryEntry): string {
  const s = entry.semantic ?? {};
  const candidates = [
    s.formcontrolname,
    s.name,
    s.aria_label,
    s.placeholder,
    s.text_content,
    s.routerlink
  ];
  for (const c of candidates) {
    if (typeof c === 'string' && c.trim().length > 0) return c;
  }
  return entry.tag;
}

function componentSlug(componentPath: string): string {
  const base = componentPath.split(/[\\/]/).pop() ?? componentPath;
  return base
    .replace(/\.component\.html$/i, '')
    .replace(/\.template\.html$/i, '')
    .replace(/\.html$/i, '');
}

/**
 * Render the Python variable name from a {@link RegistryEntry} and a template
 * string (e.g. `{component}_{element}_{key}`). Each placeholder value is
 * camelCased independently so template separators like `_` survive, yielding
 * readable names like `orderList_dropdown_customer` even when the testid
 * itself is an opaque hash.
 *
 * The final result is guaranteed to be a valid Python identifier.
 */
export function renderVariableName(
  entry: RegistryEntry,
  testid: string,
  format: string = DEFAULT_VARIABLE_FORMAT
): string {
  const hash = createHash('sha256').update(entry.fingerprint, 'utf8').digest('hex').slice(0, 6);
  const values: Record<string, string> = {
    component: camelCaseTestid(componentSlug(entry.component)),
    element: camelCaseTestid(entry.element_type),
    key: camelCaseTestid(primarySemanticValue(entry)),
    tag: camelCaseTestid(entry.tag),
    hash
  };
  const raw = renderIdTemplate(format, values);
  const sanitized = sanitizePythonIdentifier(raw);
  // If the template produced nothing useful (e.g. unknown-only placeholders),
  // fall back to the testid-based name so we never emit an empty variable.
  if (sanitized === 'tid' || sanitized.length === 0) {
    return camelCaseTestid(testid);
  }
  return sanitized;
}

/**
 * Replace any character that would be illegal in a Python identifier with
 * an underscore, collapse consecutive underscores, strip leading/trailing ones
 * and prefix with `tid_` when the result starts with a digit.
 */
function sanitizePythonIdentifier(input: string): string {
  const scrubbed = input
    .replace(/[^a-zA-Z0-9_]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_+|_+$/g, '');
  if (scrubbed.length === 0) return 'tid';
  return /^[0-9]/.test(scrubbed) ? `tid_${scrubbed}` : scrubbed;
}

/**
 * Render one Python module to a string. The output looks like:
 *
 *     # Generated by testid-gen-locators — do not edit.
 *     # Component: order-list
 *     # Run testid-gen-locators after regenerating the registry.
 *
 *     orderListThId = "xpath://*[@data-testid='order-list__th--id']"  # testid-managed
 *     orderListThKunde = "xpath://*[@data-testid='order-list__th--kunde']"  # testid-managed
 *
 * The `# testid-managed` marker is what the existing Python replacer looks
 * for — emitted files drop straight into the rename workflow.
 */
export function renderLocatorModule(mod: LocatorModule): string {
  const lines: string[] = [];
  lines.push('# Generated by testid-gen-locators — do not edit.');
  lines.push(`# Component: ${mod.component}`);
  lines.push('# Re-run testid-gen-locators after every tagger run that changes the registry.');
  lines.push('');
  for (const entry of mod.entries) {
    lines.push(`${entry.variable} = "${entry.selector}"  # testid-managed`);
  }
  lines.push('');
  return lines.join('\n');
}

export interface BuildLocatorEntryOptions {
  attributeName: string;
  xpathPrefix: string;
  /** Template for the Python variable name. Defaults to {@link DEFAULT_VARIABLE_FORMAT}. */
  variableFormat?: string;
  /**
   * When provided, the variable name is derived from `entry` via the
   * template rather than from the testid alone. This is how hash-only IDs
   * still get readable variable names.
   */
  entry?: RegistryEntry;
}

export function buildLocatorEntry(
  testid: string,
  options: BuildLocatorEntryOptions
): LocatorEntry {
  const variable =
    options.entry !== undefined
      ? renderVariableName(options.entry, testid, options.variableFormat)
      : camelCaseTestid(testid);
  return {
    variable,
    selector: xpathFor(testid, options.attributeName, options.xpathPrefix),
    testid
  };
}
